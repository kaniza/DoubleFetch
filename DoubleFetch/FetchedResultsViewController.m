- (void)configureCell:(UITableViewCell *)cell atIndexPath:(NSIndexPath *)indexPath
{
    Item *managedObject = [self.fetchedResultsController objectAtIndexPath:indexPath];
    cell.textLabel.text = managedObject.title;
    cell.accessoryType = managedObject.checked ? UITableViewCellAccessoryCheckmark : UITableViewCellAccessoryNone;
}

@end
